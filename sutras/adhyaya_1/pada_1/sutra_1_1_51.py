"""
1.1.51  उरण् रपरः  —  VIDHI

When guṇa of a vocalic **ṛ** / **ḷ** (``f`` / ``x``) yields short ``a``,
the **r** (or **l**) consonant follows (``ar``, ``al``) — operational slice
for **kar** / **har** after **7.3.84**.

पदच्छेदः  उः / अण् / रपरः

अनुवृत्तिः  षष्ठी 1.1.49, स्थाने 1.1.49

सूत्रप्रकारः (शास्त्रीय-नाम): परिभाषा (ऋकारादेशविधान-निर्णय-परिभाषा)

व्याख्या-सारः:
ऋ/ऌ-वर्णस्य स्थाने अण्-वर्णः (अ/आ/इ/ई/उ/ऊ) आदेशरूपेण विधीयमानः चेत्, तस्मात्
अण्-वर्णात् अनन्तरं नित्यम् रेफः/लकारः (यथासङ्ख्यम्) विधीयते।

प्रत्याहाराः:
  - अण् = अ, आ, इ, ई, उ, ऊ
  - र = र्, ल्

सूचना (प्रत्याहार-निर्णय):

- “अण्-प्रत्याहारः पूर्वेण णकारेण गृह्यते” — अण् अत्र **अइउण्**-सूत्रस्य
  णकारेण गृह्यते, न तु **लण्**-सूत्रस्य णकारेण। अन्यथा (दीर्घ-ॠ-ग्रहणे)
  “पितॄन् / पितॄणाम्” इत्यादिषु ॠ-आदेशस्य अपि रपरत्वं प्रसज्येत, येन
  *पितॄर्न् / पितॄर्णाम्* इत्यादि अनिष्ट-रूपाणि स्युः। अतः अण्-ग्रहणेन
  अ/आ/इ/ई/उ/ऊ-एव ग्रह्यन्ते।

- “र-प्रत्याहारेण रेफ-लकारयोः ग्रहणम्” — रपरः इति बहुव्रीहौ ‘रः’ इति
  र-प्रत्याहार-निर्देशः; अनेन र-प्रत्याहारेण रेफस्य लकारस्य च ग्रहणं
  क्रियते।

English (one-line): Any (a, ā, i, ī, u, ū) ordained as an ādeśa in place of
ṛ/ḷ is always followed by (r/l) respectively.

उदाहरणानि (ह्रस्व-ऋ-स्थाने अण्-आदेशः रपरः):

1) ब्रह्म + ऋषि (गुणैकादेशः; 6.1.87):

ब्रह्मणाम् ऋषिः
→ ब्रह्म + ऋषि   (2.2.8 षष्ठी-तत्पुरुषः; 1.2.46; 2.4.71)
→ ब्रह्म् अर् षि  (6.1.87 आद्गुणः — अ + ऋ → अर् (रपरः))
→ ब्रह्मर्षि

2) प्र + ऋच्छति (वृद्ध्येकादेशः; 6.1.91):

प्र + ऋच्छति
→ प्र + आर् च्छति  (6.1.91 उपसर्गादृति धातौ — अ + ऋ → आर् (रपरः))

3) पितृ + ङसिँ (ऋतो उत्; 6.1.111):

पितृ + ङसिँ
→ पितृ + अस्     (इत्संज्ञालोपः)
→ पित् उ र् स्    (6.1.111 ऋत उत् — उ-एकादेशः रपरः)
→ पितुर्          (8.2.24 रात्सस्य — स-लोपः)
→ पितुः           (8.3.15 खरवसानयोर्विसर्जनीयः)

उदाहरणानि (दीर्घ-ॠ-स्थाने अण्-आदेशः रपरः):

1) कॄ (ॠत इत् धातोः 7.1.100):

कॄ (विक्षेपे, तुदादिः)
→ कॄ + लट्        (3.2.123 वर्तमाने लट्)
→ कॄ + तिप्       (3.4.78 तिप्तस्झि…)
→ कृ + श + ति     (3.1.77 तुदादिभ्यः शः)
→ किरति          (7.1.100 ॠत इत् धातोः — ॠ→इ; 1.1.51 उः अण् रपरः — इकारः रपरः)

2) कॄतँ (उपधायाश्च 7.1.101):

कॄतँ (संशब्दने, चुरादिः)
→ कॄत् + णिच्       (3.1.25 … णिच्)
→ किर्ति            (7.1.101 उपधायाश्च — ॠ→इ; 1.1.51 — इकारः रपरः)
→ किर्ति + अनीयर्   (3.1.96 तव्यत्तव्यानीयरः)
→ कीर्तनीय          (8.2.78 उपधायां च — दीर्घः)

उदाहरणानि (ऌ-स्थाने अण्-आदेशः लपरः):

1) तव + ऌकारः (6.1.87):

तव + ऌकारः
→ तव् अल् कारः   (6.1.87 आद्गुणः — अ + ऌ → अल्; 1.1.51 — लपरः)
→ तवल्कारः

2) गमॢ + ङसि (6.1.111):

गमॢ + ङसि
→ गमॢ + अस्      (इत्संज्ञालोपः)
→ गम् उ ल् स्     (6.1.111 ऋत उत् — उ-एकादेशः लपरः)
→ गमुलः          (8.2.66 ससजुषो रुः; 8.3.15 खरवसानयोर्विसर्जनीयः)

दल-कृत्यम् (सूत्रपद-नियोजन-हेतवः):

1) “उः” (ऋ-शब्दस्य षष्ठी) इति किमर्थम्?
ऋ/ऌ-स्थाने अण्-आदेशे एव र/ल-परत्वं विधानम्; सर्वत्र “रपरः” इति न।
उदाहरणतया — “दाशरथि” इत्यत्र तद्धित-प्रसङ्गे दकारोत्तर-अकारस्य वृद्धिः
(7.2.117) आकारः भवति; सः **ऋ-स्थाने** न प्रवर्तते, अतः रपरत्वं न भवति —
इत्यर्थः।

दशरथस्य अपत्यम्
→ दशरथ + इञ्     (4.1.95 अत इञ्)
→ दाशरथ् + इ      (7.2.117 तद्धितेष्वचामादेः — अ→आ; 6.4.148 यस्येति च)
→ दाशरथि

2) “अण्” इति किमर्थम्?
यत्र ऋ-स्थाने “अक्” इत्यादि आदेशः विधीयते, सः अण्-प्रत्याहारान्तर्गतः न।
अतः तत्र रपरत्वं न प्रवर्तते (यद्यपि ‘अ’ घटकः अण् मध्ये अस्ति, तथापि
“अ” स्वयम् आदेशरूपेण न विधीयते)।

सुधातुः अपत्यम्
→ सुधातृ + इञ्         (4.1.97 सुधातुरकङ् च)
→ सुधात् अक् + इ       (4.1.97 अकङ्-आदेशः; 1.1.53 ङिच्च — अन्त्यादेशः)
→ सौधातकि             (7.2.117 — उ→औ)

v3 note: This sūtra is implemented as a concrete VIDHI step (it must change
the form to satisfy the R1 invariant). Selection/triggering is still kept
mechanically blind via pre-set flags on the aṅga.
"""
from __future__ import annotations

from engine       import SutraType, SutraRecord, register_sutra
from engine.state import State
from phonology    import mk


def cond(state: State) -> bool:
    if not state.terms:
        return False
    d0 = state.terms[0]
    if "dhatu" not in d0.tags:
        return False
    if d0.meta.get("urN_rapara_pending") not in ("r", "l"):
        return False
    if not d0.varnas:
        return False
    return d0.varnas[-1].slp1 == "a"


def act(state: State) -> State:
    d0 = state.terms[0]
    kind = d0.meta.get("urN_rapara_pending")
    d0.varnas.append(mk("r" if kind == "r" else "l"))
    d0.meta["urN_rapara_pending"] = None
    d0.meta["urN_rapara_done"] = True
    return state


SUTRA = SutraRecord(
    sutra_id       = "1.1.51",
    sutra_type     = SutraType.VIDHI,
    text_slp1      = "uH aR raparaH",
    text_dev       = "उः अण् रपरः",
    padaccheda_dev = "उः / अण् / रपरः",
    why_dev        = "ऋ/ऌ-प्रसङ्गे अण्-आदेशानन्तरं र्/ल्-आगमः (यथासङ्ख्यम्) — "
                     "उरण् रपरः (कृ→कर्, हृ→हर्)।",
    anuvritti_from = ("1.1.49",),
    cond           = cond,
    act            = act,
)

register_sutra(SUTRA)
