"""
1.1.50  स्थानेऽन्तरतमः  —  PARIBHASHA

पदच्छेदः  स्थाने / अन्तरतमः

सूत्रप्रकारः परिभाषासूत्रम् (आदेशचयननिर्णय-परिभाषा)

सिद्धान्तः (शास्त्रीय आशयः):

स्थानिनः अनेके आदेशाः सम्भवन्ति चेत् तेभ्यः **सदृशतमः** आदेशः एव विधीयते।
एकस्मिन् एव प्रसङ्गे यदि एकाधिकाः आदेशाः उच्यन्ते, तर्हि तेषु कः आदेशः
चेतव्यः इति निर्णयं कर्तुं **प्रकृत-परिभाषा** उपयुज्यते।

व्याख्यान-परम्परायाम् आदेश-स्थानि-सादृश्यं चतुर्विधम् इति दर्श्यते।
आदेशचयन-प्रसङ्गे एते चत्वारः प्रकाराः क्रमेण निरीक्ष्य, यस्य प्रकारस्य
अवलम्बेन सदृशतमः आदेशः निर्णेयः, तस्यैव आधारेण निर्णयः क्रियते। (एषा
विवेचना **शास्त्रीय**; v3 implementation uses explicit paribhāṣā gates.)

अनेकविधे आनन्तर्ये स्थानसाधर्म्यम् एव ग्रहीतव्यम् (परिभाषेन्दुशेखर-परम्परा):

यत्र अनेकाः आदेशाः सम्भवन्ति, तत्र **प्रथमतः उच्चारणस्थान-साधर्म्यम्**
एव द्रष्टव्यम्। स्थानसाधर्म्येण सदृशतमः आदेशः लभ्यते चेत् अन्य-साधर्म्य
(अर्थ/गुण/प्रमाण) विषयक-चिन्तनं न आवश्यकम्। एतदेव
“यत्र अनेकविधमान्तर्यम्, तत्र स्थानतः आन्तर्यं बलीयः” इति
परिभाषा-भावेनोक्तम्। अतः, मेद्यति-उदाहरणे ‘इ’ स्थाने गुण-वर्णेषु
अ-कारे प्रमाण-साधर्म्यम् अपि दृश्यते; तथापि ‘ए’ इत्यत्र स्थान-साधर्म्यं
दृढतरम् इति, तेन **ए** एव आदेशः स्वीक्रियते।

परिभाषेन्दुशेखर-निबद्धः संक्षेपः (उद्धरण-भावेन; शास्त्रीय स्मरणार्थं):

“स्थाने प्राप्यमाणानामन्तरतम आदेशो भवति सदृशतमः। कुतश्च शब्दस्यान्तर्यम्?
स्थानार्थगुणप्रमाणतः। स्थानतः — अकः सवर्णे दीर्घः ६.१.१०१ — दण्डाग्रम्।
यूपाग्रम्। द्वयोरकारयोः कण्ठ्य एव दीर्घ आकारो भवति। अर्थतः — वतण्डी चासौ
युवतिश्च वातण्ड्ययुवतिः। … गुणतः — पाकः। … प्रमाणतः — अमुष्मै। अमूभ्याम्।
… स्थान इति वर्तमाने पुनः स्थानेग्रहणं किम्? यत्रानेकमान्तर्यं संभवति तत्र
स्थानत एवान्तर्यं बलीयो यथा स्यात्। … तमब्ग्रहणं किम्? वाग् घसति। त्रिष्टुब्
भसति। झयो होऽन्यतरस्याम् ८.४.६२ इति हकारस्य पूर्वसवर्णे क्रियमाणे …”

उदाहरणम् (स्थान-सादृश्य):

“दैत्यानाम् अरिः” इति समास-प्रसङ्गे

दैत्यानाम् अरिः
→ दैत्य + अरि  (षष्ठी 2.2.8; प्रातिपदिक-संज्ञा 1.2.46; सुप्-लोप 2.4.71)
→ दैत्यारि     (अकः सवर्णे दीर्घः 6.1.101 — कण्ठ-स्थान-सादृश्यात् ‘आ’ एव)

स्थानसादृश्यस्य अपरम् उदाहरणम् (मिदेर्गुणः 7.3.82):

मिद्-धातोः इकारस्य गुण-आदेशे अ/ए/ओ इति गुण-वर्णाः सम्भाव्याः। तेषु
अ-कारस्य कण्ठः, ए-कारस्य कण्ठ-तालु, ओ-कारस्य कण्ठ-ओष्ठम् इति स्थान-भेदः।
स्थानिनः ‘इ’ (तालु) इति दृष्ट्वा, तालु-स्पर्शयुक्तः **ए** एव सदृशतमः
इति **स्थानेऽन्तरतमः**-परिभाषया चयनः क्रियते।

प्रक्रिया (शास्त्रीय-रूपेण):

ञिमिदाँ (स्नेहने, दिवादिः)
→ मिद् + लट्     (3.2.123)
→ मिद् + तिप्    (3.4.78)
→ मिद् + श्यन् + ति  (3.1.69)
→ मेद् + य + ति  (7.3.82 मिदेर्गुणः; 1.1.50 स्थानेऽन्तरतमः)
→ मेद्यति

अर्थसादृश्यम् (स्थानसादृश्ये निर्णय-अशक्ये):

यत्र स्थानसादृश्यस्य आधारेण आदेशस्य चयनं नैव कर्तुं शक्यते, तत्र
**अर्थसादृश्य** (अर्थ-समता/प्रयोजन-समता) आधारेण आदेशचयनस्य यत्नः कर्तव्यः —
भिन्नेभ्यः आदेशेभ्यः यस्य आदेशस्य अर्थः स्थानिनः सदृशः, तस्य आदेशस्य चयनः।

उदाहरणम् (वतण्डी — गोत्रापत्यार्थः):

वतण्डस्य गोत्रापत्यम्
→ वतण्ड + यञ्                (4.1.108 वतण्डाच्च)
→ वातण्ड्य + ई               (7.2.117 आदिवृद्धिः; 6.4.148 यस्येति च)
→ वातण्ड्य् + ङीन्            (4.1.73 शार्ङ्गरवाद्यञो ङीन्)
→ वतण्ड + ई                  (4.1.109 लुक् स्त्रियाम् — यञ्-लुक्)
→ वतण्ड् + ई                 (6.4.148 यस्येति च)
→ वतण्डी
→ वतण्डी + युवति            (2.1.57 विशेषणं विशेष्येण बहुलम् — कर्मधारयः)
→ वातण्ड्य + युवति          (6.3.42 पुंवत् कर्मधारयजातीयदेशीयेषु)
→ वातण्ड्ययुवति

शास्त्रीय टिप्पणी: 6.3.42-प्रसङ्गे पुंवद्भाव-लाभे “वतण्ड” तथा “वातण्ड्य” इति
द्वावपि आदेशौ सम्भाव्यौ इति मान्यते; परन्तु अत्र “वतण्डी” शब्दस्य
गोत्रापत्यार्थत्वात् अर्थेन सदृशः “वातण्ड्य” एव आदेशरूपेण स्वीक्रियते।

अर्थसादृश्यस्य अपरम् उदाहरणम् (तृज्वत् क्रोष्टुः 7.1.95):

क्रोष्टु-शब्दे तृज्वद्भाव-प्रसङ्गे (तृजन्त-रूपेषु अनेक-सम्भाव्य-आदेशेषु) अर्थेन
सदृशतमः **क्रोष्टृ** इति आदेशः स्वीक्रियते। यथा —

क्रोष्टु + औ              (प्रथमाद्विवचनस्य औ-प्रत्ययः)
→ क्रोष्टृ + औ            (7.1.95 तृज्वत् क्रोष्टुः)
→ क्रोष्टर् + औ           (7.3.110 ऋतो ङिसर्वनामस्थानयोः; 1.1.51 उरण् रपरः)
→ क्रोष्टारौ              (6.4.11 अप्तृन्तृच्स्वसृ… — उपधादीर्घः)

गुणसादृश्यम् (बाह्यप्रयत्न-सादृश्य):

स्थानसादृश्येन तथा अर्थसादृश्येन यत्र आदेशचयनं न शक्यम्, तत्र बाह्यप्रयत्नस्य
(गुणस्य) आधारेण सदृशतमः आदेशः चिन्त्यते। उदाहरणतया —

“वाक् + हरि” इति प्रसङ्गे **8.4.62 झयो होऽन्यतरस्याम्** इत्यनेन हकारस्य
पूर्वसवर्ण-आदेशे क्/ख्/ग्/घ् इति सवर्णाः सम्भाव्याः। एतेषाम् सर्वेषां स्थानं
कण्ठः इति समानम्, तथा अर्थ-विशेषोऽपि न निर्दिष्टः; अतः निर्णयार्थं
बाह्यप्रयत्नाः (घोष/अघोष, अल्प/महाप्राण, नाद/श्वास, संवार/विवार) निरीक्ष्यन्ते।
हकारस्य बाह्यप्रयत्नः (संवार-नाद-घोष-महाप्राण) घकारेण सदृशः इति निष्कर्षात्
**घ** एव आदेशरूपेण स्वीक्रियते — “वाग्घरि”।

प्रक्रिया (शास्त्रीय-रूपेण):

वाचाम् हरिः
→ वाक् + हरि      (षष्ठी 2.2.8; 1.2.46; 2.4.71)
→ वाग् + हरि      (8.2.39 झलां जशोऽन्ते)
→ वाग् + घरि      (8.4.62 झयो होऽन्यतरस्याम् — गुणसादृश्यात् घकारः)
→ वाग्घरि

गुणसादृश्यस्य अपरम् उदाहरणम् (चजोः कु घिण्ण्यतोः 7.3.52):

पच्-धातोः घञ्-प्रत्यये कृते “चजोः कु” इति कुत्व-प्रसङ्गे कवर्गस्य पञ्चापि
वर्णाः आदेशरूपेण सम्भाव्याः। तेषु चकारस्य बाह्यप्रयत्नाः
(विवार-श्वास-अघोष-अल्पप्राणः) ककारेण सह समानाः इति दृष्ट्वा
गुणसादृश्यात् **क** एव आदेशः स्वीक्रियते।

प्रक्रिया (शास्त्रीय-रूपेण):

पच् + घञ्     (3.3.18 भावे)
→ पच् + अ     (इत्संज्ञालोपः)
→ पाच् + अ    (7.2.116 अत उपधायाः)
→ पाक         (7.3.52 चजोः कु घिण्ण्यतोः — गुणसादृश्यात् ककारादेशः)

गुणसादृश्यस्य तृतीयम् उदाहरणम् (उदः स्थास्तम्भोः पूर्वस्य 8.4.61):

“उद् + स्थानम्” इति प्रसङ्गे **8.4.61 उदः स्थास्तम्भोः पूर्वस्य** इत्यनेन
सकारस्य स्थाने पूर्वसवर्ण-आदेशः विधीयते। दकारस्य सवर्णाः
त्/थ्/द्/ध्/न् इति पञ्च सम्भाव्याः; तेषु सकारस्य बाह्यप्रयत्नः
(विवार-श्वास-अघोष-महाप्राण) इति दृष्ट्वा तत्सदृशः बाह्यप्रयत्नः
यस्य अस्ति स एव (इत्युक्ते **थ**) आदेशः स्वीक्रियते।

प्रक्रिया (शास्त्रीय-रूपेण):

उद् + स्थानम्
→ उत् + स्थानम्      (8.4.55 खरि च — चर्त्वम्)
→ उत् + थ्थानम्      (8.4.61 — गुणसादृश्यात् थकारादेशः)
→ उत्थ्थानम्
→ उत्थानम्           (8.4.65 झरो झरि सवर्णे — थकारस्य विकल्पेन लोपः)

प्रमाणसादृश्यम् (उच्चारणकाल/मात्रा-सादृश्य):

यत्र स्थान/अर्थ/गुण-सादृश्यैः आदेशचयनं न सुलभम्, तत्र **प्रमाण**
(उच्चारणकालः/मात्रा) आधारेण आदेशस्य चयनः क्रियते। उदाहरणतया
“अदस्” शब्दस्य प्रक्रियायाम् **8.2.80 अदसोऽसेर्दादु दो मः** इत्यनेन
दकारस्य मकारादेशः, तथा दकारात् परस्य वर्णस्य (अ/आ) उ/ऊ-आदेशः
प्राप्नोति। “भाव्यमानोऽप्युकारः सवर्णान् गृह्णाति” इत्यादि परिभाषया
उ/ऊ उभौ सम्भाव्यौ; तत्र स्थानिनः मात्रानुसारः (ह्रस्व/दीर्घ) निर्णायकः।

उदाहरणम् 1 (दीर्घ-स्थानि → दीर्घ-आदेशः):

अदस् + भ्याम्
→ अदअ + भ्याम्      (7.2.102 त्यदादीनामः)
→ अद + भ्याम्       (6.1.97 अतो गुणे)
→ अदा + भ्याम्      (7.3.102 सुपि च — अङ्गदीर्घः)
→ अमू + भ्याम्      (8.2.80 — द→म, दीर्घ-आ-स्थाने मात्रासादृश्यात् ऊ)
→ अमूभ्याम्

उदाहरणम् 2 (ह्रस्व-स्थानि → ह्रस्व-आदेशः):

अदस् + ङे
→ अदअ + ङे          (7.2.102)
→ अद + ङे           (6.1.97)
→ अद + स्मै         (7.1.14 सर्वनाम्नः स्मै)
→ अमु + स्मै         (8.2.80 — ह्रस्व-अ-स्थाने मात्रासादृश्यात् उ)
→ अमुष्मै            (8.3.59 आदेशप्रत्यययोः — षत्वम्)

निष्कर्षः: स्थान-अर्थ-गुण-प्रमाण-सादृश्यस्य आधारे सदृशतमस्य आदेशस्य निर्णयः।

Operational v3 role: sets paribhāṣā gates so later vidhis can choose the
*sthānāntara* substitute mechanically:

- ``paribhasha_gates['sthanantara_vrddhi']``: vṛddhi selection
- ``paribhasha_gates['sthanantara_guna']``  : guṇa selection (used by 6.1.87)
"""
from __future__ import annotations

from engine       import SutraType, SutraRecord, register_sutra
from engine.state import State


def cond(state: State) -> bool:
    return (
        "sthanantara_vrddhi" not in state.paribhasha_gates
        or "sthanantara_guna" not in state.paribhasha_gates
    )


def act(state: State) -> State:
    state.paribhasha_gates.setdefault("sthanantara_vrddhi", {
        "a": "A",
        "i": "E",
        "I": "E",
        "u": "O",
        "U": "O",
    })
    # Guṇa-selection helper (used by 6.1.87 when present).
    state.paribhasha_gates.setdefault("sthanantara_guna", {
        ("a", "i"): "e",
        ("a", "I"): "e",
        ("a", "u"): "o",
        ("a", "U"): "o",
    })
    return state


